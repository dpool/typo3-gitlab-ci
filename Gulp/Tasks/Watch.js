'use strict';

/*
########################################################################################################################################################################################################
### VARIABLES ###
########################################################################################################################################################################################################
*/
var gulp = require('gulp');

var config = require('../../gulp-config');


/*
########################################################################################################################################################################################################
### FUNCTIONS ###
########################################################################################################################################################################################################
*/
gulp.task('watch',function() {
    gulp.watch(config.cssInputFilesForWatcher, gulp.series('styles'));
    gulp.watch(config.jsInputFiles, gulp.series('scripts'));
});